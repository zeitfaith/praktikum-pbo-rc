# Masukkan jumlah siswa
jumlah_siswa = int(input("Masukkan jumlah siswa: "))

# Inisialisasi dictionary untuk menyimpan data nilai siswa
nilai_siswa = {}

# Loop untuk memasukkan data nama dan nilai setiap siswa
for i in range(jumlah_siswa):
    nama = input(f"Masukkan nama siswa ke-{i+1}: ")
    nilai = int(input(f"Masukkan nilai {nama}: "))
    nilai_siswa[nama] = nilai

# Cetak dictionary nilai_siswa
print("Hasil dictionary nilai siswa:")
print(nilai_siswa)
